    public String id(String material, String marca, String modelo) {
        String id = "";
        material = txt_material.getText();
        marca = txt_marca.getText();
        modelo = txt_modelo.getText();

        String submaterial = material.substring(0, 3);

        if (marca.length() == 2) {
            marca = marca + "X";
        } else {
            if (marca.length() == 1) {
                marca = marca + "XX";
            } else {
                if (marca.length() == 0) {
                    marca = "XXX";
                }
            }
        }

        if (modelo.length() == 2) {
            modelo = modelo + "X";
        } else {
            if (modelo.length() == 1) {
                modelo = modelo + "XX";
            } else {
                if (modelo.length() == 0) {
                    modelo = "XXX";
                }
            }
        }

        String submarca = marca.substring(0, 3);
        String submodelo = modelo.substring(0, 3);

        id = submaterial + submarca + submodelo;

        return id.toUpperCase();
    }

