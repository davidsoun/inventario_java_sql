    private void btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarActionPerformed
        if (txt_id.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Primero escribe el ID de un registro.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            txt_id.requestFocus();
        } else {
            try {
                Class.forName("com.mysql.jdbc.Driver");
                DefaultTableModel dtm = new DefaultTableModel();
                table.setModel(dtm);

                dtm.setColumnIdentifiers(new Object[]{"ID", "MATERIAL", "MARCA", "MODELO", "CANTIDAD", "PRECIO"});
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost/inventario_labcel", "root", "");
                Statement comm = con.createStatement();
                ResultSet rs = comm.executeQuery("SELECT * FROM `productos` WHERE `id` LIKE '" + txt_id.getText() + "'");
                boolean flag = false;

                while (rs.next()) {
                    dtm.addRow(new Object[]{rs.getString("id"), rs.getString("material"), rs.getString("marca"), rs.getString("modelo"), rs.getInt("cantidad"), rs.getInt("precio")});
                    txt_material.setText(rs.getString("material"));
                    txt_marca.setText(rs.getString("marca"));
                    txt_modelo.setText(rs.getString("modelo"));
                    txt_cantidad.setText(Integer.toString(rs.getInt("cantidad")));
                    txt_precio.setText(Integer.toString(rs.getInt("precio")));

                    byte[] imagedata = rs.getBytes("imagen");

                    if (imagedata != null) {
                        ImageIcon format = new ImageIcon(imagedata);
                        mm = format.getImage();
                        img2 = mm.getScaledInstance(80, 80, Image.SCALE_SMOOTH);
                        image = new ImageIcon(img2);

                        btn_mimagen.setIcon(image);
                    } else {
                        icon();
                    }
                    flag = true;
                }

                if (flag == false) {
                    JOptionPane.showMessageDialog(null, "El registro no existe.", "VERIFICA", JOptionPane.QUESTION_MESSAGE);
                    txt_id.setText("");
                    txt_id.requestFocus();
                }

                if (mm == null) {
                    icon();
                }

                rs.close();

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Ocurri√≥ un error.\nContacte con el administrador." + "\nError: " + ex.toString(), "ERROR", JOptionPane.ERROR_MESSAGE);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(pnl_busqueda.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btn_buscarActionPerformed

